package modul3;

public class PerulanganDoWhile_Kel03 {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println("Halo, ini adalah perulangan DO WHILE pada Modul 3 Praktikum Pemograman Dasar 2024, " + "perulangan yang ke-" + i);
            i++;
        } while (i <= 12);
    }
}